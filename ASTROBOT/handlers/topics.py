async def process_topic_selection(callback: CallbackQuery, state: FSMContext, topic_name: str, prompt: str):
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –≤—ã–±–æ—Ä —Ç–µ–º—ã –∏ –∑–∞–ø—É—Å–∫–∞–µ—Ç –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏—é —Å —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–º –ø—Ä–æ–º–ø—Ç–æ–º.
    
    Args:
        callback (CallbackQuery): Callback query
        state (FSMContext): –ö–æ–Ω—Ç–µ–∫—Å—Ç —Å–æ—Å—Ç–æ—è–Ω–∏—è
        topic_name (str): –ù–∞–∑–≤–∞–Ω–∏–µ —Ç–µ–º—ã
        prompt (str): –ü—Ä–æ–º–ø—Ç –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω–æ–π —Ç–µ–º—ã
    """
    await callback.answer()
    
    user_id = callback.from_user.id
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –±–∞–ª–∞–Ω—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    balance = get_user_balance(user_id)
    
    if balance < MIN_REQUIRED_BALANCE:
        await callback.message.answer(
            f"‚ö†Ô∏è –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ —Å—Ä–µ–¥—Å—Ç–≤ –¥–ª—è –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏–∏.\n\n"
            f"–í–∞—à —Ç–µ–∫—É—â–∏–π –±–∞–ª–∞–Ω—Å: {balance:.0f} –±–∞–ª–ª–æ–≤\n"
            f"–ú–∏–Ω–∏–º–∞–ª—å–Ω—ã–π –±–∞–ª–∞–Ω—Å: {MIN_REQUIRED_BALANCE:.0f} –±–∞–ª–ª–æ–≤\n\n"
            f"–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø–æ–ª–Ω–∏—Ç–µ –±–∞–ª–∞–Ω—Å –≤ —Ä–∞–∑–¥–µ–ª–µ '–ë–∞–ª–∞–Ω—Å'."
        )
        return
    
    # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    profile = get_user_profile(user_id)
    if not profile:
        await callback.message.answer(
            "–í–∞—à –ø—Ä–æ—Ñ–∏–ª—å –Ω–µ –∑–∞–ø–æ–ª–Ω–µ–Ω. –î–ª—è –∑–∞–ø–æ–ª–Ω–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö –≤—ã–±–µ—Ä–∏—Ç–µ '–ò–∑–º–µ–Ω–∏—Ç—å –º–æ–∏ –¥–∞–Ω–Ω—ã–µ' –∏–ª–∏ –≤–≤–µ–¥–∏—Ç–µ /start."
        )
        return
    
    # –ê–∫—Ç–∏–≤–∏—Ä—É–µ–º —Ä–µ–∂–∏–º –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏–∏, –µ—Å–ª–∏ –æ–Ω –µ—â–µ –Ω–µ –∞–∫—Ç–∏–≤–µ–Ω
    data = await state.get_data()
    if not data.get("in_consultation", False):
        await start_consultation_mode(user_id, state)
    
    # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ Holos, –µ—Å–ª–∏ –æ–Ω–∏ —É–∂–µ –µ—Å—Ç—å –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–∏
    holos_data = data.get("holos_response", {})
    
    # –ï—Å–ª–∏ –¥–∞–Ω–Ω—ã—Ö Holos –Ω–µ—Ç, –ø–æ–ª—É—á–∞–µ–º –∏—Ö –∑–∞–Ω–æ–≤–æ
    if not holos_data:
        # –§–æ—Ä–º–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫—É –¥–∞—Ç—ã –∏ –≤—Ä–µ–º–µ–Ω–∏ —Ä–æ–∂–¥–µ–Ω–∏—è
        date_str = f"{profile['birth_date']} {profile['birth_time']}"
        latitude = profile["latitude"]
        longitude = profile["longitude"]
        altitude = profile["altitude"]
        
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å –∫ API Holos
        await callback.message.answer("–ü–æ–ª—É—á–∞—é –¥–∞–Ω–Ω—ã–µ –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞... –≠—Ç–æ –º–æ–∂–µ—Ç –∑–∞–Ω—è—Ç—å –Ω–µ—Å–∫–æ–ª—å–∫–æ —Å–µ–∫—É–Ω–¥.")
        
        response_data = await send_request_to_holos(
            holos_url=HOLOS_DREAM_URL,
            date_str=date_str,
            latitude=latitude,
            longitude=longitude,
            altitude=altitude
        )
        
        # –°–æ–±–∏—Ä–∞–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏
        user_profile_info = (
            f"–î–∞—Ç–∞ —Ä–æ–∂–¥–µ–Ω–∏—è: {profile['birth_date']}\n"
            f"–í—Ä–µ–º—è —Ä–æ–∂–¥–µ–Ω–∏—è: {profile['birth_time']}\n"
            f"–ú–µ—Å—Ç–æ —Ä–æ–∂–¥–µ–Ω–∏—è (–∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã): {latitude}, {longitude}, {altitude}\n"
            f"–î–∞–Ω–Ω—ã–µ API: {response_data}"
        )
        
        holos_data = {
            "user_profile": user_profile_info,
            "api_response": response_data
        }
        
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –¥–∞–Ω–Ω—ã–µ –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–∏
        await state.update_data(holos_response=holos_data)
    
    # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –æ—Ç–≤–µ—Ç —Å –≤—ã–±—Ä–∞–Ω–Ω—ã–º –ø—Ä–æ–º–ø—Ç–æ–º
    await callback.message.answer(f"üìã –í—ã –≤—ã–±—Ä–∞–ª–∏ —Ç–µ–º—É: {topic_name}")
    
    # –°–æ–∑–¥–∞–µ–º –æ—Ç–≤–µ—Ç –Ω–∞ –æ—Å–Ω–æ–≤–µ –≤—ã–±—Ä–∞–Ω–Ω–æ–π —Ç–µ–º—ã - —É–±—Ä–∞–ª–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä type_shown
    expert_comment = answer_with_rag(
        prompt,
        holos_data,
        mode="free",
        conversation_history="",
        max_tokens=2000
    )
    
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –æ—Ç–≤–µ—Ç
    from services.db import save_message
    save_message(user_id, 'bot', expert_comment)
    
    await callback.message.answer(
        expert_comment,
        reply_markup=get_end_consultation_keyboard()
    )

